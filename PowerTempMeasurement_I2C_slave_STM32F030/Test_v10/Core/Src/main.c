	  uint32_t sum = 0;
	  for (int i = 0; i < 100; i++) {
		  for(int i = 0; i < 400; i++) {
		      __NOP();
		  }
		  sum += ADC_buffer[2];
	  }
	  uint16_t raw_i = (uint16_t)(sum / 100);

	  // scale trước hoặc sau lookup đều được (tùy logic bạn)
	  uint16_t scaled_v = (uint32_t)raw_v * 1489/43*44  /ADC_buffer[3];


	  uint16_t scaled_t = (uint32_t)raw_t * 1489 / ADC_buffer[3];
	  uint16_t scaled_i = (uint32_t)raw_i * 1489/14*13/ ADC_buffer[3];


	  Voltage = lookup_interpolated(voltage_5bit, scaled_v);
	  Temp    = lookup_interpolated(temp_5bit, scaled_t);
	  Current = lookup_interpolated(current_5bit, scaled_i);

	  static uint8_t myData[7];

	   myData[0] = Voltage >> 8;
	   myData[1] = Voltage & 0xFF;
	   myData[2] = Current >> 8;
	   myData[3] = Current & 0xFF;
	   myData[4] = Temp >> 8;
	   myData[5] = Temp & 0xFF;

	   myData[6] = myData[0]^myData[1]^myData[2]^myData[3]^myData[4]^myData[5];

	   I2C_SetTxData(myData);
	   HAL_Delay(10);
