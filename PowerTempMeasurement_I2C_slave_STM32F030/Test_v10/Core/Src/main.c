uint16_t lookup_interpolated(const uint16_t *table, uint16_t index_12bit) {
    uint8_t i = index_12bit >> 7;        // Lấy 5 bit cao
    uint8_t f = index_12bit & 0b000001111111;       // Lấy 7 bit thấp

    uint16_t y0 = table[i];
    uint16_t y1 = table[i + 1];


    return y0 + (uint16_t)(((int32_t)(y1 - y0) * f) >> 7);
}
